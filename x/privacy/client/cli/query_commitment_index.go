package cli

import (
	"context"

	"github.com/cosmos/cosmos-sdk/client"
	"github.com/cosmos/cosmos-sdk/client/flags"
	"github.com/spf13/cobra"
	"privacy/x/privacy/types"
)

func CmdListCommitmentIndex() *cobra.Command {
	cmd := &cobra.Command{
		Use:   "list-commitment-index",
		Short: "list all CommitmentIndex",
		RunE: func(cmd *cobra.Command, args []string) error {
			clientCtx := client.GetClientContextFromCmd(cmd)

			pageReq, err := client.ReadPageRequest(cmd.Flags())
			if err != nil {
				return err
			}

			queryClient := types.NewQueryClient(clientCtx)

			params := &types.QueryAllCommitmentIndexRequest{
				Pagination: pageReq,
			}

			res, err := queryClient.CommitmentIndexAll(context.Background(), params)
			if err != nil {
				return err
			}

			return clientCtx.PrintProto(res)
		},
	}

	flags.AddPaginationFlagsToCmd(cmd, cmd.Use)
	flags.AddQueryFlagsToCmd(cmd)

	return cmd
}

func CmdShowCommitmentIndex() *cobra.Command {
	cmd := &cobra.Command{
		Use:   "show-commitment-index [index]",
		Short: "shows a CommitmentIndex",
		Args:  cobra.ExactArgs(1),
		RunE: func(cmd *cobra.Command, args []string) (err error) {
			clientCtx := client.GetClientContextFromCmd(cmd)

			queryClient := types.NewQueryClient(clientCtx)

			argIndex := args[0]

			params := &types.QueryGetCommitmentIndexRequest{
				Index: argIndex,
			}

			res, err := queryClient.CommitmentIndex(context.Background(), params)
			if err != nil {
				return err
			}

			return clientCtx.PrintProto(res)
		},
	}

	flags.AddQueryFlagsToCmd(cmd)

	return cmd
}
